def random_ua() -> str:
    """Return a random realistic browser user agent string."""
    return random.choice(_UAS)


def make_session() -> requests.Session:
    """Create a requests.Session with a randomized browser UA and Accept headers.

    Use this instead of bare requests.get/Session() in all protopen_scripts
    to avoid leaving an identifiable 'protopen-*' fingerprint on target servers.
    """
    session = requests.Session()
    ua = random_ua()
    hdrs = random.choice(_ACCEPT_HEADERS).copy()
    hdrs["User-Agent"] = ua
    session.headers.update(hdrs)
    return session


def make_headers() -> dict[str, str]:
    """Return a headers dict with a random browser UA (for one-shot requests)."""
    hdrs = random.choice(_ACCEPT_HEADERS).copy()
    hdrs["User-Agent"] = random_ua()
    return hdrs
